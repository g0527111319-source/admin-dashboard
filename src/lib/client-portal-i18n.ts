export type Lang = "he" | "ar" | "en";
export type TranslationKey = keyof typeof translations.he;

export function getClientLang(): Lang {
  if (typeof window === "undefined") return "he";
  return (localStorage.getItem("client-portal-lang") as Lang) || "he";
}

export function setClientLang(lang: Lang) {
  localStorage.setItem("client-portal-lang", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "en" ? "ltr" : "rtl";
}

export function ct(key: TranslationKey, lang?: Lang): string {
  const l = lang || getClientLang();
  return translations[l]?.[key] || translations.he[key] || key;
}
